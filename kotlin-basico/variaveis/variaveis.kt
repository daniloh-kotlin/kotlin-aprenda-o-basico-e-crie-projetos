fun main() {

    // Variáveis

    // Var - Variável mutável
    var count: Int = 10
    count = 15

    println("variável count: " + count)


    // Val - Variável imutável
    val languageName: String = "Kotlin"

    println("variável languageName: " + languageName)


    // Alguns tipos
    var texto: String = "Sou uma string"
    var inteiro: Int = 100
    var decimalDouble: Double = 100.00
    var decimalFloat: Float = 100f

    println()
    println("=== Tipos: String, Inteiro, Double e Float ===")
    println("Variável texto: " + texto) 
    println("Variável inteiro: " + inteiro)
    println("Variável decimalDouble: " + decimalDouble)
    println("Variável decimalFloat: " + decimalFloat)
}